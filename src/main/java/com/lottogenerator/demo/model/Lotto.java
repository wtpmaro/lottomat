package com.lottogenerator.demo.model;


public class Lotto {

    Integer first;

    Integer second;

    Integer third;

    Integer fourth;

    Integer fiveth;

    Integer sixth;

    public Lotto () {}

    public Integer getFirst() {
        return first;
    }

    public void setFirst(Integer first) {
        this.first = first;
    }

    public Integer getSecond() {
        return second;
    }

    public void setSecond(Integer second) {
        this.second = second;
    }

    public Integer getThird() {
        return third;
    }

    public void setThird(Integer third) {
        this.third = third;
    }

    public Integer getFourth() {
        return fourth;
    }

    public void setFourth(Integer fourth) {
        this.fourth = fourth;
    }

    public Integer getFiveth() {
        return fiveth;
    }

    public void setFiveth(Integer fiveth) {
        this.fiveth = fiveth;
    }

    public Integer getSixth() {
        return sixth;
    }

    public void setSixth(Integer sixth) {
        this.sixth = sixth;
    }
}
